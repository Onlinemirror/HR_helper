"""
Создаёт шаблонные .docx из заполненных образцов, заменяя реальные данные на {{ПЕРЕМЕННЫЕ}}.
Запустить один раз: python create_templates.py
Готовые Шаблон_*.docx загрузить в папку «Шаблоны» на Google Drive.
"""
import io, re
from docx import Document


def _replace_para(para, replacements: dict) -> None:
    full = "".join(r.text for r in para.runs)
    new = full
    for src, dst in replacements.items():
        new = new.replace(src, dst)
    if new != full and para.runs:
        para.runs[0].text = new
        for run in para.runs[1:]:
            run.text = ""


def make_template(src_path: str, dst_path: str, replacements: dict) -> None:
    doc = Document(src_path)
    for para in doc.paragraphs:
        _replace_para(para, replacements)
    for table in doc.tables:
        for row in table.rows:
            for cell in row.cells:
                for para in cell.paragraphs:
                    _replace_para(para, replacements)
    doc.save(dst_path)
    print(f"✅  {dst_path}")


